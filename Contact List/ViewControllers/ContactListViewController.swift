class ContactListViewController: UITableViewController {
    private var contacts: [Contact] = []

    override func viewDidLoad() {
        super.viewDidLoad()

        // Uncomment the following line to preserve selection between presentations
        // self.clearsSelectionOnViewWillAppear = false

        // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
        // self.navigationItem.rightBarButtonItem = self.editButtonItem
        fetchContacts()
        
        tableView.rowHeight = 50
    }

    private func fetchContacts() {
        NetwokManager.shared.fetchContacts { contacts in
            DispatchQueue.main.async {
                self.contacts = contacts
                self.tableView.reloadData()
            }
        }
    }

    // MARK: - Table view data source

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        // #warning Incomplete implementation, return the number of rows
        contacts.count
    }

    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "cell", for: indexPath)
        var content = cell.defaultContentConfiguration()

        content.text = contacts[indexPath.row].name.fullName
        content.imageProperties.cornerRadius = 50
        DispatchQueue.global().async {
            let imageData = NetwokManager.shared.fetchImage(from: self.contacts[indexPath.row].picture.large)!
            DispatchQueue.main.async {
                content.image = UIImage(data: imageData)
                
                cell.contentConfiguration = content
            }
        }

        cell.contentConfiguration = content
        return cell
    }

